DB_PORT = os.getenv('DB_PORT', '3306')

DATABASES = {
    'default': {
        'ENGINE': 'django.db.backends.mysql',
        'NAME': DB_NAME,
        'USER': DB_USER,
        'PASSWORD': DB_PASSWORD,
        'HOST': DB_HOST,
        'PORT': DB_PORT,
        'OPTIONS': {
            'charset': 'utf8mb4',
            'init_command': "SET sql_mode='STRICT_TRANS_TABLES'",
        }
    }
}

# Auto-creation of MySQL DB and Fallback Check
try:
    import MySQLdb
    # Attempt connecting to MySQL engine directly to check and create database if needed
    db_conn = MySQLdb.connect(
        host=DB_HOST,
        user=DB_USER,
        passwd=DB_PASSWORD,
        port=int(DB_PORT) if DB_PORT else 3306,
        connect_timeout=3
    )
    cursor = db_conn.cursor()
    cursor.execute(f"CREATE DATABASE IF NOT EXISTS {DB_NAME} CHARACTER SET utf8mb4 COLLATE utf8mb4_unicode_ci;")
    db_conn.close()
    print("Database checked: MySQL is online and database is ready.")
except Exception as e:
    print(f"Warning: MySQL connection failed ({e}). Falling back to SQLite3 database.")
    DATABASES = {
        'default': {
            'ENGINE': 'django.db.backends.sqlite3',
            'NAME': BASE_DIR / 'db.sqlite3',
        }
    }

